export async function getCircuits(season = '2024') {
    const res = await fetch(`https://ergast.com/api/f1/${season}/circuits.json`);
    const data = await res.json();
    const circuits = data.MRData.CircuitTable.Circuits;
    console.log(circuits, "circuits data", data.MRData.CircuitTable.Circuits.length)
    const enrichedCircuits = await Promise.all(
        circuits.map(async (circuit) => {
            const wikiTitle = circuit.url.split('/').pop();
            try {
                const wikiRes = await fetch(`https://en.wikipedia.org/api/rest_v1/page/summary/${wikiTitle}`);
                const wikiData = await wikiRes.json();
                return {
                    ...circuit,
                    thumbnail: wikiData.thumbnail?.source || null
                };
            } catch {
                return {
                    ...circuit,
                    thumbnail: null
                };
            }
        })
    );

    return enrichedCircuits;
}

export async function getCircuit(circuitId) {
    const res = await fetch(`http://ergast.com/api/f1/circuits/${circuitId}.json`);
    const data = await res.json();
    const circuit = data.MRData.CircuitTable.Circuits[0];
    const wikiTitle = circuit.url.split('/').pop();

    try {
        const wikiRes = await fetch(`https://en.wikipedia.org/api/rest_v1/page/summary/${wikiTitle}`);
        const wikiData = await wikiRes.json();

        return {
            ...circuit,
            thumbnail: wikiData.originalimage?.source || null,
            description: wikiData.extract || null,
        };
    } catch (error) {
        console.error(`Error fetching wiki data for ${wikiTitle}`, error);
        return {
            ...circuit,
            thumbnail: null,
            description: null,
        };
    }
}
